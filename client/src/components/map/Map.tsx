type Shock = {
  id: number;
  timestamp: string;
  zAccel: number;
  userId: number;
  latitude: number;
  longitude: number;
  altitude: number;
};

type Route = {
  id: number;
  user_id: number;
  latitude: number[];
  longitude: number[];
  altitude: number[];
};

export type MapComponentProps = {
  routes: Route[];
  shocks: Shock[];
};

const MapComponent: React.FC<MapComponentProps> = ({ routes, shocks }) => {
  

  const mapRef = React.useRef(null);

  const userId = localStorage.getItem('userId');


  return (
    <div className="flex justify-center text-center align-middle w-full h-[100vh]">
      <MapContainer
        ref={mapRef}
        className="map"
        center={[44.8, -0.6]}
        zoom={12}
        scrollWheelZoom={true}
        style={{width: '100%', height: '100%',  zIndex: 30}}
      >
        <TileLayer
          attribution='&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors'
          url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png"
        />

        {shocks.map((shock: Shock) => (
          <Circle
            center={[shock.latitude, shock.longitude]}
            radius={1}
            color={
              shock.userId === Number(userId)
                ? 'green'
                : Math.abs(shock.zAccel) > 4
                  ? 'red'
                  : 'blue'
            }
          />
        ))}

        {routes.map((route) => (
          <Polyline
            pathOptions={{ color: 'blue' }}
            positions={route.latitude.map((lat, i) => [
              lat,
              route.longitude[i],
            ])}
          />
        ))}
      </MapContainer>
    </div>
    

  );
};

export default MapComponent;

